    Create function func with function arguments 'a' and 'b'
    String type argument a and b are given. Return True if both arguments are of equal length.
    Args:
        a: string
        b: string
    Returns:
        True or False
*/
bool func(String a, String b) {
  int a1, b1;
  a1 = a.length;
  b1 = b.length;
  bool x;
  if (x = a1 == b1) {
    return x;
  } else {
    return x;
  }
}

